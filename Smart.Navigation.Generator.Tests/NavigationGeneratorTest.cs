    [Fact]
    public void BasicViewSourceGeneratesPartialMethod()
    {
        const string source =
            """
            using System;
            using System.Collections.Generic;
            using Smart.Navigation.Attributes;

            namespace Test;

            public enum ViewId
            {
                Form1,
                Form2
            }

            [View(ViewId.Form1)]
            public sealed class Form1
            {
            }

            [View(ViewId.Form2)]
            public sealed class Form2
            {
            }

            public static partial class ViewRegistry
            {
                [ViewSource]
                public static partial IEnumerable<KeyValuePair<ViewId, Type>> ListViews();
            }
            """;

        var generated = GeneratorTestHelper.GetGeneratedSource(source);

        Assert.Contains("partial class ViewRegistry", generated, StringComparison.Ordinal);
        Assert.Contains("public static partial global::System.Collections.Generic.IEnumerable<global::System.Collections.Generic.KeyValuePair<global::Test.ViewId, global::System.Type>> ListViews()", generated, StringComparison.Ordinal);
    }

