    //-----------------------------------------------------------------------
    // Multiple
    //-----------------------------------------------------------------------

    [Fact]
    public void MultipleViewsGenerateMultipleYieldReturns()
    {
        const string source =
            """
            using System;
            using System.Collections.Generic;
            using Smart.Navigation.Attributes;

            namespace Test;

            public enum ViewId
            {
                Form1,
                Form2
            }

            [View(ViewId.Form1)]
            public sealed class Form1
            {
            }

            [View(ViewId.Form2)]
            public sealed class Form2
            {
            }

            public static partial class ViewRegistry
            {
                [ViewSource]
                public static partial IEnumerable<KeyValuePair<ViewId, Type>> ListViews();
            }
            """;

        var generated = GeneratorTestHelper.GetGeneratedSource(source);

        Assert.Contains("typeof(global::Test.Form1)", generated, StringComparison.Ordinal);
        Assert.Contains("typeof(global::Test.Form2)", generated, StringComparison.Ordinal);
        Assert.Equal(2, CountOccurrences(generated, "yield return"));
    }

